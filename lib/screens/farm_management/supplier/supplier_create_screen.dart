                  _addressController,
                  'ƒê·ªãa ch·ªâ',
                  validator: (value) {
                    if (value == null || value.isEmpty) {
                      return 'Vui l√≤ng nh·∫≠p ƒê·ªãa ch·ªâ';
                    }
                    return null;
                  },
                ),
                const SizedBox(height: 16),
                _buildTextField(
                  _phoneController,
                  'S·ªë ƒëi·ªán tho·∫°i',
                  type: TextInputType.phone,
                  validator: (value) {
                    if (value == null || value.isEmpty) {
                      return 'Vui l√≤ng nh·∫≠p S·ªë ƒëi·ªán tho·∫°i';
                    }
                    if (!RegExp(r'^\d{10}$').hasMatch(value)) {
